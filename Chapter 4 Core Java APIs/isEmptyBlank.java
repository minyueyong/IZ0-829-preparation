public class isEmptyBlank{


	public static void main ( String [] args ){

		System.out.println ( " ".isEmpty( ) );    //false    not empty because there is a whitespace
		System.out.println ( "".isEmpty( ) );     //true
		System.out.println ( " ".isBlank( ) );     //true
		System.out.println ( "".isBlank( ) );      //true


	}



}